using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using System.Linq;

public class KPlayer : MCharacter
{
    // Start is called before the first frame update
    [SerializeField]
    List<MonsterSpawner> MonsterSpawn;

 

    public Dictionary<string, SkillInfo> MySkills = new Dictionary<string, SkillInfo>();            //현재 플레이어가 가지고 있는 스킬들
    public override void Update()
    {
        base.Update();


        TargetCheck();
    }

    public void TargetCheck()
    {
        if (MState != State.Attack&&
            isAuto)           //공격 중이 아니고 자동 이동이라면
        {
            foreach (var spawner in MonsterSpawn)
            {
                if (Target == null)
                {
                    foreach (var item in spawner.MonsterPool)
                    {
                        if (item.activeSelf)
                        {
                            Target = item;
                            MState = State.Move;

                      
                            break;
                        }
                    }
                }
                
            }
        }
    }
}
